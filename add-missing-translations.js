// Function to add translations before the closing of each language section
function addTranslations(lang, langName) {
  const pattern = new RegExp(`(\\t\\tfailedToUpdate: [^\\n]+\\n)(\\t\\}\\),\\n\\t${langName}: withOverrides\\(\\{)`);
  const match = content.match(pattern);
  
  if (match) {
    content = content.replace(pattern, `$1${additions[lang]}\n$2`);
    console.log(`‚úÖ Added translations for ${lang.toUpperCase()}`);
    return true;
  } else {
    console.log(`‚ùå Pattern not found for ${lang.toUpperCase()}`);
    return false;
  }
}

